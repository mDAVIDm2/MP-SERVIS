/// Ключ API «Поиск по организациям» (Geosearch) — красные метки (автосервисы, мойки, шиномонтажи из Яндекса).
///
/// Кто указывает ключ: только разработчик/сборка приложения. Один ключ встраивается в приложение при сборке.
/// Обычные пользователи приложения ничего не указывают — они просто ставят приложение и видят карту (с красными точками, если ключ был указан при сборке).
///
/// Как получить ключ:
/// 1. Зайти на https://developer.tech.yandex.ru/
/// 2. Подключить API → выбрать категорию «Карты» → «API Поиска по организациям» (Organization Search).
/// 3. Создать ключ и подставить его в команду сборки.
///
/// Запуск/сборка с ключом:
///   flutter run --dart-define=YANDEX_MAPS_API_KEY=ваш_ключ
/// Важно: нужен ключ именно «API Поиска по организациям» + оплаченный тариф. Иначе 403.
/// Без ключа/лицензии Яндекс используются организации из OpenStreetMap (Overpass API), бесплатно.
///
/// Альтернатива: 2ГИС — демо-ключ на 1000 запросов/месяц (platform.2gis.ru, Places API).
const String yandexMapsApiKey = String.fromEnvironment(
  'YANDEX_MAPS_API_KEY',
  defaultValue: '', // Подставьте ключ от «API Поиска по организациям» или: flutter run --dart-define=YANDEX_MAPS_API_KEY=ключ
);

bool get hasYandexMapsApiKey => yandexMapsApiKey.isNotEmpty;
